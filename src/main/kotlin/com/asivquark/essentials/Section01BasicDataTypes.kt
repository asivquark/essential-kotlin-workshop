package com.asivquark.essentials

object Section01BasicDataTypes {
    
    // Return integer numeric variable
    fun task1(): Any {
        return 1982
    }

    // Return string variable
    fun task2(): Any {
        return "Jordi"
    }

    // Return character variable
    fun task3(): Any {
        return 'C'
    }

    // Return short precision floating-point variable
    fun task4(): Any {
        return 182.5f
    }

    // Return large precision floating-point variable
    fun task5(): Any {
        return 3.141592654
    }

    // Return boolean variable
    fun task6(): Any {
        return true
    }

    // Return array of characters
    fun task7(): Any {
        return arrayOf('c', 'o', 's', 'm', 'i', 'c')
    }
}